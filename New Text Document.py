# Создаём презентацию
prs = Presentation()

# Функция для добавления слайда с заголовком и текстом
def add_slide(title, content_lines, layout_idx=1):
    slide_layout = prs.slide_layouts[layout_idx]
    slide = prs.slides.add_slide(slide_layout)
    slide.shapes.title.text = title
    
    # Очищаем существующий текст в body
    body = slide.placeholders[1]
    text_frame = body.text_frame
    text_frame.clear()
    
    for line in content_lines:
        p = text_frame.add_paragraph()
        p.text = line
        p.font.size = Pt(20)
        p.space_after = Pt(10)

# Функция для добавления таблицы на слайд
def add_table_slide(title, headers, rows):
    slide_layout = prs.slide_layouts[5]  # Пустой слайд
    slide = prs.slides.add_slide(slide_layout)
    
    # Добавляем заголовок
    title_box = slide.shapes.add_textbox(Inches(0.5), Inches(0.2), Inches(9), Inches(0.8))
    title_box.text = title
    title_box.text_frame.paragraphs[0].font.size = Pt(32)
    title_box.text_frame.paragraphs[0].font.bold = True
    
    # Добавляем таблицу
    rows_count = len(rows) + 1
    cols_count = len(headers)
    table = slide.shapes.add_table(rows_count, cols_count, Inches(0.5), Inches(1.2), Inches(9), Inches(3)).table
    
    # Заголовки
    for i, header in enumerate(headers):
        cell = table.cell(0, i)
        cell.text = header
        cell.text_frame.paragraphs[0].font.bold = True
        cell.text_frame.paragraphs[0].font.size = Pt(18)
    
    # Данные
    for row_idx, row in enumerate(rows):
        for col_idx, val in enumerate(row):
            cell = table.cell(row_idx + 1, col_idx)
            cell.text = str(val)
            cell.text_frame.paragraphs[0].font.size = Pt(16)

